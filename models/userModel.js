const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  isAdmin: {
    type: Boolean,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  userName: {
    type: String,
    required: false,
  },
  address: {
    type: String,
    required: false,
  },
  phoneNumber: {
    type: String,
    required: false,
  },
  joinedTime: {
    type: Date,
    required: false,
  },
});

module.exports = mongoose.model('User', userSchema);
